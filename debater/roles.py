def get_role(role_id: str) -> DebateRole:
    """获取角色定义，无效 ID 回退到 bear（保守默认）"""
    return ROLES.get(role_id, ROLES["bear"])


def get_role_display_name(role_id: str, model_id: str = "") -> str:
    """生成角色显示名，如 '🐂 支持者 · kimi_k2.6'"""
    role = get_role(role_id)
    if model_id:
        return f"{role.emoji} {role.name} · {model_id}"
    return f"{role.emoji} {role.name}"
